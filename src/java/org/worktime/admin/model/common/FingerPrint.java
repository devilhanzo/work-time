package org.worktime.admin.model.common;

/**
 *
 * @author -
 */
public class FingerPrint {
    private String hip_no;
    private String hip_name;
    private String no;
    private String empname;
    private String department;
    private String expire;

    public String getHip_no() {
        return hip_no;
    }

    public void setHip_no(String hip_no) {
        this.hip_no = hip_no;
    }

    public String getHip_name() {
        return hip_name;
    }

    public void setHip_name(String hip_name) {
        this.hip_name = hip_name;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getEmpname() {
        return empname;
    }

    public void setEmpname(String empname) {
        this.empname = empname;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getExpire() {
        return expire;
    }

    public void setExpire(String expire) {
        this.expire = expire;
    }
}
